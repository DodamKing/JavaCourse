package t2_escape;

public class Test1 {
	public static void main(String[] args) {
		System.out.println("안녕하세요");
		System.out.println("안녕\t하세요");
		System.out.println("안녕\n하세요");
		System.out.println();
		
		System.out.println("안녕 철수야!");
		System.out.println("안녕 \'철수\'야!");
		System.out.println("안녕 \\철수\"야!");
		
	}
}
