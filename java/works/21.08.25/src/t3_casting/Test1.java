package t3_casting;

public class Test1 {
	public static void main(String[] args) {
		int i1 = 10;
		int i2 = Integer.parseInt("20");
		System.out.println("i2 : " + i2);
		
		
		double j1 = 3.14;
		double j2 = Double.parseDouble("3.14");
		System.out.println("j2 : " + j2);
		
		boolean k1 = true;
		boolean k2 = Boolean.getBoolean("true");
		System.out.println("k2 : " + k2);
		
	}
}
